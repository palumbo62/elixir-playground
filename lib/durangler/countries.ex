defmodule Durangler.Countries do
  @moduledoc """
    The Countries context which implement a set of helper
    functions used to interface to the attached database
    using the Repo module.

    This module also includes the Currencies context as 
    both contexts are tightly coupled.  This context provides
    a similar set of helper functions as with Countries.
  """
  import Ecto.Query, warn: false
  alias Durangler.Repo

  alias Durangler.Countries.Currency

  @doc """
  Returns the list of currencies.

  ## Examples

      iex> list_currencies()
      [%Currency{}, ...]

  """
  def list_currencies do
    Repo.all(Currency)
  end

  @doc """
  Gets a single currency.

  Raises `Ecto.NoResultsError` if the Currency does not exist.

  ## Examples

      iex> get_currency!(123)
      %Currency{}

      iex> get_currency!(456)
      ** (Ecto.NoResultsError)

  """
  def get_currency(id), do: Repo.get(Currency, id)

  def get_currency!(id), do: Repo.get!(Currency, id)

  @doc """
  Gets a single currency by currency code

  Raises `Ecto.NoResultsError` if the Currency does not exist.

  ## Examples

      iex> get_currency_by_code!("EUR")
      %Currency{}

      iex> get_currency_by_code!("XYZ")
      ** (Ecto.NoResultsError)

  """
  def get_currency_by_code!(code) do
    query =
      from(c in Currency,
        where: c.code == ^code
      )

    Repo.one!(query)
  end

  @doc """
  Creates a currency.

  ## Examples

      iex> create_currency(%{field: value})
      {:ok, %Currency{}}

      iex> create_currency(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_currency(attrs \\ %{}) do
    %Currency{}
    |> Currency.changeset(attrs)
    |> Repo.insert()
  end

  @doc """
  Updates a currency.

  ## Examples

      iex> update_currency(currency, %{field: new_value})
      {:ok, %Currency{}}

      iex> update_currency(currency, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_currency(%Currency{} = currency, attrs) do
    currency
    |> Currency.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a currency.

  ## Examples

      iex> delete_currency(currency)
      {:ok, %Currency{}}

      iex> delete_currency(currency)
      {:error, %Ecto.Changeset{}}

  """
  def delete_currency(%Currency{} = currency) do
    Repo.delete(currency)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking currency changes.

  ## Examples

      iex> change_currency(currency)
      %Ecto.Changeset{data: %Currency{}}

  """
  def change_currency(%Currency{} = currency, attrs \\ %{}) do
    Currency.changeset(currency, attrs)
  end

  alias Durangler.Countries.Country

